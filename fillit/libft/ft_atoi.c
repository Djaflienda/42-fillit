#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	result;
	long	check;
	char	sign;

	i = 0;
	result = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		check = result;
		result = (result * 10) + str[i] - 48;
		if (result < check)
			return (sign < 0 ? 0 : -1);
		i++;
	}
	return ((long int)result * sign);
}
