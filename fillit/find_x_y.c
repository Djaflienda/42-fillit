#include "fillit.h"

int find_x_y_max(t_list *list)
{
	int i;

   i = 3;
   while (i >=0)
   {
	   if (ft_strchr(list->mtx[i], '#'))
	   {
	   		list->y_max = i + 1;
			break ;
	   }
	   i--;
   }
   i = 3;
   while (i >= 0)
   {
	   if (list->mtx[0][i] == '#' || list->mtx[1][i] == '#'
	   ||list->mtx[2][i] == '#' ||list->mtx[3][i] == '#')
	   {
		   list->x_max = i + 1;
		   break ;
	   }
	   i--;
   }
	return(0);
}
